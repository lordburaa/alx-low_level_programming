/**
 * add - add number
 * @a: int a
 * @b: int b
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substraction
 * @a: int a
 * @b: in b
 * Return: substraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplicatoin
 * @a: int a
 * @b: in b
 * Retrun: multiplicaton of the number
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - modules
 * @a: int a
 * @b: in b
 * Return: modules
 */
int mod(int a, int b)
{
	return (a % b);
}
